package com.ftd.cart.vo;

/**
 * The Class Personalization.
 * @author Mohit Singh
 */
public class Personalization {
	
	/**The personalizationType **/
	private String personalizationType;
	
	/**The personalizationName **/
	private String personalizationName;
	
	/**The personalizationDisplayValue **/
	private String personalizationDisplayValue;
	
	/**The personalizationValue **/
	private String personalizationValue;
	
	/**The fulfillmentSortOrder **/
	private String fulfillmentSortOrder;
	
	/**The iconImageUrl **/
	private String iconImageUrl;

	/**
	 * @return the personalizationType
	 */
	public String getPersonalizationType() {
		return personalizationType;
	}

	/**
	 * @param personalizationType the personalizationType to set
	 */
	public void setPersonalizationType(String personalizationType) {
		this.personalizationType = personalizationType;
	}

	/**
	 * @return the personalizationName
	 */
	public String getPersonalizationName() {
		return personalizationName;
	}

	/**
	 * @param personalizationName the personalizationName to set
	 */
	public void setPersonalizationName(String personalizationName) {
		this.personalizationName = personalizationName;
	}

	/**
	 * @return the personalizationDisplayValue
	 */
	public String getPersonalizationDisplayValue() {
		return personalizationDisplayValue;
	}

	/**
	 * @param personalizationDisplayValue the personalizationDisplayValue to set
	 */
	public void setPersonalizationDisplayValue(String personalizationDisplayValue) {
		this.personalizationDisplayValue = personalizationDisplayValue;
	}

	/**
	 * @return the personalizationValue
	 */
	public String getPersonalizationValue() {
		return personalizationValue;
	}

	/**
	 * @param personalizationValue the personalizationValue to set
	 */
	public void setPersonalizationValue(String personalizationValue) {
		this.personalizationValue = personalizationValue;
	}

	/**
	 * @return the fulfillmentSortOrder
	 */
	public String getFulfillmentSortOrder() {
		return fulfillmentSortOrder;
	}

	/**
	 * @param fulfillmentSortOrder the fulfillmentSortOrder to set
	 */
	public void setFulfillmentSortOrder(String fulfillmentSortOrder) {
		this.fulfillmentSortOrder = fulfillmentSortOrder;
	}

	/**
	 * @return the iconImageUrl
	 */
	public String getIconImageUrl() {
		return iconImageUrl;
	}

	/**
	 * @param iconImageUrl the iconImageUrl to set
	 */
	public void setIconImageUrl(String iconImageUrl) {
		this.iconImageUrl = iconImageUrl;
	}

}
